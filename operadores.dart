void main(){
  //declarando variables
  int num1=12;
  int num2 = 23;

  //suma
  int suma = num1 + num2; 
  print("Suma: $suma");  
//resta
  int resta = num1 - num2; 
  print("Resta: $resta");
//multiplicación 
  int multiplicacion = num1 * num2; 
  print("multiplicación: $multiplicacion");  

  //división
  double division = num1 / num2; 
  print("División: $division");  

  //Dividión entera
  int divisionEntera = num1 ~/ num2;
  print("División entera: $divisionEntera");

  //Módulo
  int modulo = num1 % num2;
  print("Módulo (residuo): $modulo");
}